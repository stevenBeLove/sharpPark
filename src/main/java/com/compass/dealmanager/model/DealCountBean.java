package com.compass.dealmanager.model;

/**
 * @author gan
 * 
 */
public class DealCountBean {

	private Integer counts;
	private Double accountCount;
	private Double feeAmtCount;

	public Integer getCounts() {
		return counts;
	}

	public void setCounts(Integer counts) {
		this.counts = counts;
	}

	public Double getAccountCount() {
		return accountCount;
	}

	public void setAccountCount(Double accountCount) {
		this.accountCount = accountCount;
	}

	public Double getFeeAmtCount() {
		return feeAmtCount;
	}

	public void setFeeAmtCount(Double feeAmtCount) {
		this.feeAmtCount = feeAmtCount;
	}

}
